// loops.js - Demonstrating Loops in JavaScript

// 1️⃣ for loop - Repeats code a specific number of times
console.log("\nFor loop example:");
for (let i = 0; i < 5; i++) {
    console.log("Iteration:", i);
}

// 2️⃣ while loop - Repeats code while a condition is true
console.log("\nWhile loop example:");
let count = 0;
while (count < 3) {
    console.log("Count is:", count);
    count++;
}

// 3️⃣ do-while loop - Always runs the code block once, then checks the condition
console.log("\nDo-while loop example:");
let number = 0;
do {
    console.log("Number is:", number);
    number++;
} while (number < 2);

